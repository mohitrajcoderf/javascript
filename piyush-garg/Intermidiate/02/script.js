// logical operators.
// && - AND

const age = 20;
const gender = 'male';

if(age >= 18 && gender == 'male'){
        console.log("You can vote");
}
else{
        console.log("You cannot vote");
}

// || - OR;

let Age = 2;
let Gender = 'female';

if(Age >= 18 || Gender == 'male'){
        console.log("You can vote");
}
else{
        console.log("You cannot vote");
}


// ! - NOT

const number = 10;

if(number % 2 != 0){
        console.log("Number is odd");
}
else{
        console.log("Number is even");
}

// ! is used to convert a boolean value to its opposite boolean value.